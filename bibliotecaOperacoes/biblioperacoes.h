float soma (float* n1, float* n2)
{
    float soma = *n1 + *n2;
    return soma;
}

float subtracao (float* n1, float* n2)
{
    float subtracao = *n1 - *n2;
    return subtracao;
}

float mutiplicacao (float* n1, float* n2)
{
    float multiplicacao = *n1 * *n2;
    return multiplicacao;
}

float divisao (float* n1, float* n2)
{
    float divisao = *n1 / *n2;
    return divisao;
}